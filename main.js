let paragraph = document.querySelector('.unvisible_CBCD_paragraph')
console.log(paragraph.style)
function showParagraph()
{
if(paragraph.style.display==='none') 
  paragraph.style.display = 'block';
 else
  paragraph.style.display = 'none';
}

